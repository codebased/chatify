package com.example.layoutdesign.Model;

public class ChatContact {

	private String chatContactID;
	private String name;
	
	public void setChatContactID(String id)
	{
		this.chatContactID = id;
	}
	
	public void setName(String name){
		this.name = name;
	}
	
	public String getChatContactID()
	{
		return this.chatContactID;
	}
	
	public String getName()
	{
		return this.name;
	}
}
	
	 